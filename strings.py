#Example 1 Accessing characters (Indexing)
text = "Python"
print(text[0])   # First character: P
print(text[-1])  # Last character: n

#Example 2 Slicing strings
word = "Programming"
print(word[0:6])   # 'Progra'
print(word[3:])    # 'gramming'
print(word[-6:-1]) # 'ammin'

#Example 3 String repetition & concatenation
a = "Hi"
b = "There"
print(a + " " + b)  # 'Hi There'
print(a * 4)        # 'HiHiHiHi'
