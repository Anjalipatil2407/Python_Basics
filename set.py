# Creating and printing a set
numbers = {1, 2, 3, 4, 5}

print("Numbers Set:")
print(numbers)
print("Type:", type(numbers))

# Adding and removing elements in a set
fruits = {"apple", "banana", "cherry"}

# Add element
fruits.add("orange")

# Remove element
fruits.remove("banana")

print("Updated Fruits Set:")
print(fruits)
