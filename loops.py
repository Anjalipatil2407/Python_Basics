# Using a for loop to print numbers from 1 to 5
for i in range(1, 6):
    print("Number:", i)
# Using a while loop to print numbers from 1 to 5
count = 1
while count <= 5:
    print("Count:", count)
    count += 1
# Looping through a list
fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print("Fruit:", fruit)
